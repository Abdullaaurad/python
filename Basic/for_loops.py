print("\nOne to Nine =",end="")
for i in range(10):
    print(i,end=" ")

print("\nOdd numbers =",end="")
for i in range(0,10,2):
    print(i,end=" ")

print("\nArray print =",end="")
Array={2,4,5.2,7,8}
for item in Array:
    print(item,end=" ")

print("\nReverse =",end="")
for i in range(10,3,-2):
    print(i,end=" ")
